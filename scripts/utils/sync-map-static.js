/**
 * Build map-portal (Vite) → map-static/ + optional mapStaticBundle.js
 *
 * Usage: node scripts/utils/sync-map-static.js
 */
const { execSync } = require("child_process");
const fs = require("fs");
const path = require("path");

const ROOT = path.resolve(__dirname, "../..");
const APP = path.join(ROOT, "map-portal");
const DIST = path.join(APP, "dist");
const OUT = path.join(ROOT, "map-static");
const BUNDLE = path.join(ROOT, "src/features/map/mapStaticBundle.js");

function rmrf(dir) {
  if (!fs.existsSync(dir)) return;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, entry.name);
    if (entry.isDirectory()) rmrf(p);
    else fs.unlinkSync(p);
  }
  fs.rmdirSync(dir);
}

function copyDir(src, dest) {
  fs.mkdirSync(dest, { recursive: true });
  for (const entry of fs.readdirSync(src, { withFileTypes: true })) {
    const from = path.join(src, entry.name);
    const to = path.join(dest, entry.name);
    if (entry.isDirectory()) copyDir(from, to);
    else fs.copyFileSync(from, to);
  }
}

function walkFiles(dir, base = dir, acc = []) {
  if (!fs.existsSync(dir)) return acc;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, entry.name);
    if (entry.isDirectory()) walkFiles(p, base, acc);
    else acc.push({ key: path.relative(base, p).replace(/\\/g, "/"), full: p });
  }
  return acc;
}

const MIME = {
  ".html": "text/html; charset=utf-8",
  ".css": "text/css; charset=utf-8",
  ".js": "application/javascript; charset=utf-8",
  ".mjs": "application/javascript; charset=utf-8",
  ".svg": "image/svg+xml",
  ".json": "application/json; charset=utf-8",
  ".woff": "font/woff",
  ".woff2": "font/woff2",
  ".png": "image/png",
  ".jpg": "image/jpeg",
  ".jpeg": "image/jpeg",
  ".webp": "image/webp",
  ".ico": "image/x-icon",
};

const TEXT_EXTS = new Set([".html", ".css", ".js", ".mjs", ".svg", ".json", ".txt", ".map"]);

function main() {
  if (!fs.existsSync(path.join(APP, "package.json"))) {
    console.error("map-portal/package.json missing");
    process.exit(1);
  }

  const nodeModules = path.join(APP, "node_modules");
  if (!fs.existsSync(nodeModules)) {
    console.log("Installing map-portal dependencies…");
    // Prefer system npm with --prefix (repo npm.cmd shim always targets monorepo root)
    const sysNpm =
      process.platform === "win32"
        ? `"${path.join(process.env.ProgramFiles || "C:\\Program Files", "nodejs", "npm.cmd")}"`
        : "npm";
    try {
      execSync(`${sysNpm} install --prefix "${APP}"`, { cwd: ROOT, stdio: "inherit", shell: true });
    } catch {
      execSync(`npm install --prefix "${APP}"`, { cwd: ROOT, stdio: "inherit", shell: true });
    }
  }

  console.log("Building map-portal…");
  const tscBin = path.join(
    APP,
    "node_modules",
    ".bin",
    process.platform === "win32" ? "tsc.cmd" : "tsc"
  );
  const viteBin = path.join(
    APP,
    "node_modules",
    ".bin",
    process.platform === "win32" ? "vite.cmd" : "vite"
  );
  execSync(`"${tscBin}" --noEmit`, { cwd: APP, stdio: "inherit", shell: true });
  execSync(`"${viteBin}" build`, { cwd: APP, stdio: "inherit", shell: true });

  if (!fs.existsSync(DIST)) {
    console.error("Build produced no dist/");
    process.exit(1);
  }

  if (fs.existsSync(OUT)) rmrf(OUT);
  copyDir(DIST, OUT);

  const files = walkFiles(OUT, OUT);
  const bundle = {};
  for (const { key, full } of files) {
    const ext = path.extname(full).toLowerCase();
    if (!TEXT_EXTS.has(ext)) continue;
    bundle[key] = {
      contentType: MIME[ext] || "application/octet-stream",
      body: fs.readFileSync(full, "utf8"),
    };
  }

  fs.mkdirSync(path.dirname(BUNDLE), { recursive: true });
  fs.writeFileSync(
    BUNDLE,
    `// AUTO-GENERATED by scripts/utils/sync-map-static.js — do not edit\nexport const MAP_STATIC_BUNDLE = ${JSON.stringify(bundle, null, 0)};\n`,
    "utf8"
  );

  console.log(`map-static: ${files.length} files → ${OUT} (${Object.keys(bundle).length} in text bundle)`);
}

main();
